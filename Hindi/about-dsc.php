          <br/>
          <p>भारत को एक वैश्विक महाशक्ति और हमारे देश के युवाओं के प्रेरणा शक्ति एक ही लक्ष्य को हासिल करने के लिए है के रूप में वृद्धि करने के लिए अपार क्षमता है। मैं पहली बार एक भारतीय होने में बहुत गर्व है, और मैं अपने राष्ट्र की कल्पना को कामयाब करने के लिए सभी बाधाओं और चुनौतियों के खिलाफ। देश के एक नागरिक के रूप में, यह मेरा कर्तव्य और जिम्मेदारी चुनौतियों मेरे साथी नागरिकों द्वारा सामना उन्मूलन की दिशा में काम करने के लिए है; जो ऊपर बढ़ रहा है और वैश्विक मंच पर उज्ज्वल चमक की ओर से विकास को सीमित कर रहे हैं।</p>

<p>मैं हमेशा से हमारे देश के युवाओं को सलाह दी है, कि उनकी उत्पादकता और सफलता प्राप्त करने की संभावना है, काफी वृद्धि, अगर ईमानदारी से एक के प्रयासों का केंद्र है। तो पूरी ईमानदारी और निष्ठा के साथ, मैं जिम्मेदारी लेने के लिए एक बेहतर कल के लिए आज के युवाओं के लिए तैयार है। और मुझे पूरा विश्वास है कि अब समय है। यह भ्रष्टाचार और छल जैसे गैर उत्पादक तत्वों के लिए समय राष्ट्र से पूरी तरह से साफ हो रहा है। यह देश के लिए शब्द राजनीति को फिर से परिभाषित करने के लिए समय है। यह सार्वजनिक सेवा हमारे ध्यान का केन्द्र बिन्दु बनाने के लिए समय है। और ऐसा करने के लिए, हम सब अब शुरू करनी चाहिए।</p>

<p>यह मेरे लिए एक बहुत कुछ अवसरों का विशुद्ध रूप से आत्मविश्वास और दृढ़ संकल्प की कमी के कारण, हमारे देश के युवा और संभावित नागरिकों द्वारा याद किया जा रहा है देखने के लिए पीड़ा होती है। यह दर्द मुझे वहाँ अभी भी निर्दोष लोगों की जान, जो बुनियादी सुविधाओं से वंचित हैं की इतनी भारी संख्या में हैं कि देखने के लिए। देश भर में युवा और संभावित मन, विशेषकर ग्रामीण क्षेत्रों में बेहतर हमारे देश के उत्थान के लिए उपयोग किया जा रहा नहीं कर रहे हैं। वहाँ विश्वास, दृढ़ संकल्प का एक मजबूत कमी है और एक जलती हुई इच्छा के ऊपर सबसे अधिक अपने राष्ट्र के प्रति योगदान करने के लिए। यह अत्यंत महत्वपूर्ण है देश के लिए एक साथ आते हैं और इन बाधाओं के खिलाफ लड़ने के लिए। एक युवाओं के बीच जागरूकता की खोज करने और जिसे उपयुक्त अपनी क्षमता का अनुमान बनाने की जरूरत है। एक अपने आप में एक मजबूत दृढ़ संकल्प और विश्वास पहले, और फिर किसी के सपने में है; वहाँ इस दुनिया में कुछ भी नहीं है जो आप को रोक सकता है।</p>
