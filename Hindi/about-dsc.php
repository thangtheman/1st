          <p>नाम - दूरदर्शी, परोपकारी और एक नेता - डॉ सुभाष चंद्र (डीएससी), बस तीन सरल शब्दों में संक्षेप किया जा सकता है।</p>

<p>एक नेता, जो अपने संभावित राष्ट्र का पोषण करने के लिए, और एक वैश्विक मंच के लिए यह तरक्की साबित हो गया है। एक नेता, जो सभी बाधाओं के खिलाफ लड़ने के लिए, और चुनौतियों का सामना करना पड़ा राष्ट्र द्वारा हल करने की शक्ति है। एक नेता, जो बेहद, 90 के दशक में भारत की उदारीकरण कहानी के लिए योगदान दिया है जो अपने आप में राष्ट्र न सिर्फ अपनी पहली निजी उपग्रह टेलीविजन चैनल है, लेकिन एक पूरे उद्योग को उपहार देने, रोजगार के लाखों बनाने के द्वारा।</p>

<p>डीएससी की बुद्धि, दृढ़ संकल्प, और दृष्टि, हर भारतीय को सशक्त करने के लिए हमारे देश की धारणा के स्तर में बदलाव लाने की खोज में अपार क्षमता है; और देश भर में एक बहुत आवश्यक परिवर्तन।</p>

<p>डीएससी, एक स्वयं बनाया आदमी, लगातार नए कारोबार की पहचान और सफलता के पथ पर उन्हें नेतृत्व करने के लिए अपनी क्षमता का प्रदर्शन किया है। उन्होंने DSCShow है, जो देश भर में 230 से अधिक Mn दर्शकों ने देखा है पर अपने समृद्ध अनुभव के शेयरों। इस लोकप्रिय शो के साथ, वह अब देश के युवाओं के लिए एक मार्गदर्शक बल है, समृद्ध और उनके जीवन को सशक्त बनाने।</p>
